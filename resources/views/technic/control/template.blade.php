    <li>Gaz taqsimlash punkti (GTP) qurish zarurligi</li>
    <li>
        <b>
            Tabiiy gazni yonilg&#8216;i sifatida foydalanuvchi iste&#8217;molchilar kuz-qish mavsumida 3 oy davomida extiyojni qoplaydigan muqobil yoqilg&#8216;i
            zaxirasi va havo harorati pasaygan davrlarda texnologik jarayonni uzluksiz ravishda muqobil yoqilg&#8216;ida ishlashiga moslashgan bo&#8216;lishi shart.
        </b>
    </li>
    <li>
        <b>Qish mavsumida aholi va ijtimoiy soha obyektlari tabiiy gaz ta&#8217;minotida tanqislik kuzatilgan davrda boshqa iste&#8217;molchilarning tabiiy gaz ta&#8217;minotiga kafolat berilmaydi.</b>
    </li>
    <li>
        Loyihalashtirish va gazlashtirish ishalari buyurtmachi hisobidan amalga oshiriladi.
    </li>
    <li>
        Loyihalash ishlari maxsus ruxsatnomaga ega bo&#8216;lgan vakolatli loyihalash korxonalari tomonidan amaldagi SHNK talablari doirasida ishlab chiqilishi,
        shuningdek mavjud iste&#8217;molchilar va istiqbolni inobatga olishi shart.
    </li>
    <li>
        Tabiiy gazdan asosiy yoqilg&#8216;i turi sifatida foydalanishning aniq davri (<u>aprel-oktabr</u>) (<i>turar joy qismi bundan mustasno</i>)
    </li>
    <li>
        <span>Loyihada quyidagi shartlar bajarilsin:</span>
        <ol type="a" class="px-3">
            <li>
                gaz tarmog&#8216;ining ulanish nuqtasi ko&#8216;rsatilib, ulanish nuqtasida "O&#8216;zstandart" Agentligi elektron manzilining o&#8216;lchov vositalari davlat
                reestriga kiritilgan va yashil rang bilan belgilangan o&#8216;lchov vositasini o&#8216;rnatish;
            </li>
            <li>
                ulanish nuqtasidagi tabiiy gazning maksimal soatlik, kunlik, yillik sarfi va ishchi bosimi, shuningdek, ushbu bosimning o&#8216;zgarish chegaralari;
            </li>
            <li>
                zamonaviy sertifikatlangan energiyatejamkor va foydali ish quvvati yuqori bo&#8216;lgan gaz jihozlari va yondirgichlarni o&#8216;rnatish,
                gazdan foydalanish uskunasi va gaz gorelkali qurilmalarga qo&#8216;yiladigan talablar (turar va soni), muvofiqlik sertifikatining mavjudligi;
            </li>
            <li>
                gaz taqsimlash shahobchasi (keyingi o&#8216;rinlarda GTSH deb ataladi) inshootini qurish zarurligi, GTSHga ega bo&#8216;lgan texnik talablar,
                avtomatika turi, gaz quvurlari soni va ularning har biridagi tabiiy gaz bosimi, chiqishdan himoyalash chora-tadbirlari va boshqalar;
            </li>
            <li>qo&#8216;shimcha gaz quvurlarini qurish ishalari;</li>
            <li>zaxira yoqilg&#8216;idan foydalanish davrini ko&#8216;rsatgan holda, zaxira yoqilg&#8216;i xo&#8216;jaligini qurish zarurligi;</li>
            <li>gaz jihoz o&#8216;rnatiladigan xonada gazasignalizator uskunasini o&#8216;rnatish;</li>
            <li>elektrokimoyoviy himoya yoki protektor qurilma oxo&#8216;rnatish (yer osti gaz quvuri qurilgan taqdirda);</li>
            <li>tabiiy gazni yoqilg&#8216;i sifatida foydalanuvchi iste&#8217;molchilarga kuz-qish mavsumida 3 oy davomida ehtiyojini qoplaydigan muqobil yoqilg&#8216;i zaxirasi va texnologik
                jarayonni avtomatik ravishta muqobil yoqilg&#8216;ida ishlashini ta&#8217;minlash.</li>
        </ol>
    </li>
    <li>
        <strong>Gaz tarmoqlariga ulash loyihasi gaz tarmoqlarini qurish va ularadan foydalanish bo&#8216;yicha berilgan texnik shartga
            muvofiq ishlab chiqilib, loyihachi tomonidan shahar/tuman gaz bo&#8216;limida ro&#8216;yxatdan o&#8216;tkazilishi shart.</strong>
    </li>
    <li>Yiliga 100 000 va undan ortiq kub. metr tabiiy gazdan birra uskuna uchun yoqilg&#8216;i sifatida foydalnuvchi yuridik shaxslar gaz xo&#8216;jaliging
        mavsumiy va uzoq muddat ishlovchi gaz iste&#8217;moli obyektlarini ishga tushurish uchun ruxsatnomalar &#8220;Sanoat-xavsizlik davlat qo&#8216;mitasi&#8221;
        va &#8220;O&#8216;zdavgazinspeksiya&#8221; tomonidan qonun hujjatlarida belgilangan tartibda beriladi, ushbu ruxsatnomalarsiz gazdan foydalanish uskunasiga
        gaz berish taqiqlanadi.
    </li>
    <li>
        Ushbu hududdagi keyingi iste&#8217;molchilar tabiiy gaz bilan gazlashtirishda yuqoridagi tabiiy gaz quvuriga ulanishi shart.
    </li>
    <li>
        Gazlashtirish texnik shartning amal qilish muddati berilgan kundan boshlab 2 (ikki) yilni tashkil qiladi. Belgilangan muddat o&#8216;tgach,
        agar iste&#8217;molchi gazlashtirishni tugatmagan bo&#8216;lsa, gazlashtirish uchun yangi texnik shart olishi lozim.
    </li>
    <li>
        Texnik shart Vazirlar Mahkamasining 2018-yilning 12-yanvardagi 22-son va 2018-yil 31-martdagi 256-son hamda 2019-yil 16-martdagi
        226-sonli qarorlari talablari doirasida ishlab chiqildi. (Gazlashtirish loyiha-texnik hujjatlari ushbu qarorlar hujjatlari asosida bajarilsin.)
    </li>
